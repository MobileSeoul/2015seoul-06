	public StationInfo isContain(float imgX, float imgY, double rate) {
		float dx = 20, dy = 20;
		dx /= rate;
		dy /= rate;
		imgX *= rate;
		imgY *= rate;
		float stationX, stationY;
		for (int i = 0; i < stationInfo.size(); i++) {
			stationX = (float) (stationInfo.get(i).getX());
			stationY = (float) (stationInfo.get(i).getY());
			Log.i("stationX", String.valueOf(stationX));
			Log.i("stationY", String.valueOf(stationY));
			if (imgX < stationX + dx && imgX > stationX - dx && imgY < stationY + dy && imgY > stationY - dy) {
				Log.i("yes", stationInfo.get(i).getName());
				return stationInfo.get(i);
			}
		}
		return null;
	}

